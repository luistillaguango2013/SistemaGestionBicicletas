//Este metodo permite iniciar sesi√≥n a los usaurios registrados en la base de datos con sus respectivas credenciales
    void IniciarSesionUsuario() {
        String[] titulos = {"Codigo", "Rol"};
        String[] registros = new String[2];
        String usu = Txt_Usuario.getText();
        String cla = PasswordTxt.getText();
        String usubd = "", clabd = "";
        int idusu = 0;
        String sql = "";
        sql = "select * from usuarios where usuario='" + usu + "' and clave=sha1('" + cla + "') ";
        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                idusu = Integer.parseInt(rs.getString("Id_usuario"));
                usubd = rs.getString("usuario");
                clabd = rs.getString("clave");
            }
            if (usu.equals(usubd)) {

                FrmSeguridadDoblellave inf = new FrmSeguridadDoblellave();
                inf.LabelIdUsu.setText(String.valueOf(idusu));
                inf.LabelUsuarioRegistrado.setText(usubd);
                inf.setVisible(true);
                dispose();
            } else {
                JOptionPane.showMessageDialog(null, " Error, el usuario  o clave no son correctas...!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }

