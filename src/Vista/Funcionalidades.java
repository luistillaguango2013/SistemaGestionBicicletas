    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // Este metodo permite registrar la funcionalidad como el de administra prestamos o de estaciones de servicios.
        String funcionalidad;
        String sql = "";
        funcionalidad = Txt_funcionalidad.getText();
        Modelo.Cls_Funcionalidades Funt=new Cls_Funcionalidades(funcionalidad);
        sql = "Insert into funcionalidades(Funcionalidad) values (?)";
        try {
            PreparedStatement pst = cn.prepareStatement(sql);
            pst.setString(1, Funt.getStr_funcionalidades());
            int n = pst.executeUpdate();
            if (n > 0) {
                JOptionPane.showMessageDialog(null, "Se registro la funcionalidad correctamente...!");
                limpiar();
                bloquear();
                cn.close();
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

