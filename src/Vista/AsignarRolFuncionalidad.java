    //Este metodo le permite buscar una funcionalidad dentro de la base de datos para verificar que tipo de perfil le corresponde al usaurio manipular
    void cargarFuncionalidad() {

        String[] titulos = {"Codigo", "Funcionalidad"};
        String[] registros = new String[2];
        String sql = "";
        sql = "select * from funcionalidades";
        modelofuncionalidad = new DefaultTableModel(null, titulos);
        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                registros[0] = String.valueOf(rs.getInt("Id_funcionalidades"));
                registros[1] = rs.getString("Funcionalidad");
                modelofuncionalidad.addRow(registros);
            }
            Table_Funcionalidades.setModel(modelofuncionalidad);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }

//Este método permite buscar un rol que esta almacenado dentro de la base de datos
    void cargarRoles() {
        String[] titulos = {"CodigoRoles", "CodigoUsuario", "Usuario"};
        String[] registros = new String[3];
        String sql = "";
        sql = "select * from rolusuario,usuarios where rolusuario.Id_usuario=usuarios.Id_usuario";
        model = new DefaultTableModel(null, titulos);
        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                registros[0] = String.valueOf(rs.getInt("Id_rolusuario"));
                registros[1] = String.valueOf(rs.getInt("Id_usuario"));
                registros[2] = rs.getString("usuario");
                model.addRow(registros);
            }
            Table_Rol.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }

