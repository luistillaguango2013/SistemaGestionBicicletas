    private void btnAsignarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAsignarActionPerformed
        // Este método permite asignar un funcionalidad al rol del usuario
        String idrol;
        String rol;
        String funcionalidad;
        String idfuncionalidad;
        String sql = "";
        idfuncionalidad = TxtCodigoFunc.getText();
        funcionalidad = Txt_Funcionalidad.getText();
        idrol = TxtCodigoRol.getText();
        rol = TxtRol.getText();
        sql = "Insert into rol_funcionalidades(Id_rolusuario,Id_funcionalidades) values (?,?)";
        try {
            PreparedStatement pst = cn.prepareStatement(sql);
            pst.setString(1, idrol);
            pst.setString(2, idfuncionalidad);
            int n = pst.executeUpdate();
            if (n > 0) {
                JOptionPane.showMessageDialog(null, "Se Asigno el Rol de: " + rol + " al Funcionalidad: " + funcionalidad + " de forma correcta");
//                limpiar();
//                bloquear();
                cn.close();
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnAsignarActionPerformed

